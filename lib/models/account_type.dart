enum AccountType {
  /// Plan d'Épargne en Actions
  PEA,

  /// Compte-Titres Ordinaire
  CTO,

  /// Assurance Vie
  AssuranceVie,

  /// Plan Épargne Retraite
  PER,

  /// Portefeuille de crypto-monnaies
  Crypto,

  /// Autre type de compte
  Autre,
}
